<#
.SYNOPSIS
Scheduler script to setup a scheduled task for sequencing
#>

param(
    [bool]$cmdlet,
    [string]$user,
    # The name of the application, e.g., notepadplusplus, etc.
    [string]$appName,
    [string]$installer,
    [string]$installerOptions,
    [string]$update,
    [int]$timeoutInMinutes
)

# the name for the scheduled task.
$TASK_NAME = "sequencer_task"

function enableRDP
{
    set-ItemProperty -Path 'HKLM:\System\CurrentControlSet\Control\Terminal Server' -name "fDenyTSConnections" -Value 0
    Enable-NetFirewallRule -DisplayGroup "Remote Desktop"
    set-ItemProperty -Path 'HKLM:\System\CurrentControlSet\Control\Terminal Server\WinStations\RDP-Tcp' -name "UserAuthentication" -Value 1
}

function ScheduleSequencerLaunchTask
{
    $TaskAction = New-ScheduledTaskAction -Execute "sequencer.exe"

    $trigger = New-ScheduledTaskTrigger -AtLogon
    $principal = New-ScheduledTaskPrincipal -UserId $user -RunLevel Highest
    $scheduledTask = Register-ScheduledTask -TaskName $TASK_NAME -Trigger $trigger -Action $TaskAction -description "Launch the Sequencer" -Principal $principal
}

function ScheduleSequencingTask
{
    $publicDocFolder = "$env:Systemdrive\Users\Public\Documents"
    $taskScriptName = "runner.ps1"

    $taskScriptFile = "$publicDocFolder\$taskScriptName"
    $TaskAction = New-ScheduledTaskAction -Execute "PowerShell.exe" -Argument "-ExecutionPolicy Bypass -File `"$taskScriptFile`" -appName `"$appName`" -installer `"$installer`" -installerOptions `"$installerOptions`" -update $update"

    $trigger = New-ScheduledTaskTrigger -AtLogon
    $principal = New-ScheduledTaskPrincipal -UserId $user -RunLevel Highest
    $scheduledTask = Register-ScheduledTask -TaskName $TASK_NAME -Trigger $trigger -Action $TaskAction -description "start the sequencing" -Principal $principal
}

enableRDP
if ($cmdlet)
{
    ScheduleSequencingTask
}
else
{
    ScheduleSequencerLaunchTask
}

